package com.demo.vo;

public class DepartmentEmployeeVo {
	private String employeeID;
	private String employeeName;
	private String headshipName;
	private String headshipLevel;
	private String sex;
	private int age;
	public String getEmployeeID() {
		return employeeID;
	}
	public void setEmployeeID(String employeeID) {
		this.employeeID = employeeID;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getHeadshipName() {
		return headshipName;
	}
	public void setHeadshipName(String headshipName) {
		this.headshipName = headshipName;
	}
	public String getHeadshipLevel() {
		return headshipLevel;
	}
	public void setHeadshipLevel(String headshipLevel) {
		this.headshipLevel = headshipLevel;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
